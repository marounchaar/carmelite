   <h3>المدارس الكرملية في لبنان</h3>
    <p>آلاف الخريجين وقدامى طلاب مدارس الآباء الكرمليّين في لبنان حول العالم،<br>
استقر رهبان الكرمل في طرابلس منذ عام 1645 واسسوا في قلب المدينة مدرسة في العام 1857 وهي مدرسة مار الياس للآباء الكرمليين والتي عرفت باسم مدرسة "الطليان". وبموازاة ازدهار مدينة طرابلس منتصف القرن الماضي، استقبلت "الطليان" تلامذتها من ابناء المناطق المجاورة: زغرتا، الكورة، جبة بشري، الضنية وعكار، وكذلك من البلدان العربية والخليجية. وازداد عدد الطلاب المقيمين في داخل المدرسة (المدرسة الداخلة)؛ وتميّزت مدرسة "الطليان" حينها بالنشاطات الرياضية، وريادتها لمختلف البطولات على صعيد لبنان والشمال.<br>
ومنذ دخولهم الى القبيات سنة 1836 اسسوا مدرسة في القبيات، عرفت بمدرسة تحت السنديانة في البداية، ومن ثم بنوا بمحاذات الديرمدرسة صغيرة وهي مدرسة مارضوميط للآباء الكرمليين واليوم أصبحت صرحاً ضخماً ساهم في تربية غالبية أبناء المنطقة العكارية.
في اواخر الخمسينيات قام الرهبان ببناء مدرسة ثالثة في بيروت في الحازمية على اسم سيدة الكرمل إلا أنها تحولت في أواخر التسعينات الى ادارة علمانية<br>
وفي سنة 1971 قرر الآباء بناء مدرسة جديدة تستوعب الأعداد المتزايدة من الطلاب في منطقة مجدليا التي تربط طرابلس والضنية وزغرتا، وبنوها مدرسةً حديثة جداً، واسمها "الكرملية". كانت تحتوي قاعات كبيرة وملاعب متعددة، محاطة بغابات من الصنوبر والشربين، إلا انها دمرت بالكامل سنة 1976 بسبب الحرب. إلا ان الرهبان أصروا على اعادة بنائها واستمروا في رسالتهم التعليمية ونشر المعرفة والإنفتاح والمحبة بين الجميع.
<br>
مع مطلع 2010 تم الاتفاق على توحيد اسماء المدارس والتي باتت تعرف جميعها باسم
    <br><span>"مدارس الكرملية"</span></P>
    <P><span class="bold">:
    رسالة الأب ريمون عبدو الرئيس الاقليمي لرهبان الكرمل في لبنان لطلاب مدارس الكرملية
    <br></span>
    لقد غلبنا الجهل وانتصرنا، وعبرنا الحرب ومن تحت الركام صعدنا... وكنت أنت، أيها الطالب الحبيب، في قلب تلك السنوات المتتالية، تنمو وتكبر لكي تصل الى حيث أنت
    <br> 

    اليوم نعود الى الذكريات لأن في قلوبنا شوق، ليس الى الماضي، بل الى قلوبٍ أحبت وتحب من أجل بناء الإنسان والوطن.
    مدارس الكرمليّة في طرابلس، القبيات، الحازمية ومجدليا، هي بانتظار أن نعيد سوية بناء ما تمكنت منه الحرب والأيام الصعبة. سوية يمكننا أن نعيد كتابة سطور في حياة الوطن والإنسان
</p>
